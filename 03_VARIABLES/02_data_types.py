# Python supports several built_in data types:
# Integers (int): Whole numbers (e.g., 10, -5).
# Floats (float): Decimal numbers (e.g., 3.4, -0.001).
# Strings (str): Text data enclosed in qoutes (e.g., "Hello", 'Pythojn').
# Booleans (bool): Represents True of False.
# Lists: Ordered, mustable collections (e.g., [1, 2, 3]).
# Tuples: Ordered, immutable collections (e.g., {1, 2, 3}).
# Sets: Unordered collections of unique elements (e.g., {1, 2, 3}).
# Dictionaries: Key-value pairs (e.g., {"name": "Alice", "age": 25}).

age = 3
print(age)
print(type(age))

cgpa = 8.2
print(cgpa)
print(type(cgpa))

name = "Raju"
print(name)
print(type(name))

is_completed = True # can also be False
print(is_completed)
print(type(is_completed))